#[derive(Debug)]
pub enum HtmlError {
    MissingMarker,
    InvalidStructure(String),
    Io(std::io::Error),
}

impl From<std::io::Error> for HtmlError {
    fn from(value: std::io::Error) -> Self {
        Self::Io(value)
    }
}

impl std::fmt::Display for HtmlError {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            Self::MissingMarker => write!(f, "missing generated HTML marker"),
            Self::InvalidStructure(message) => write!(f, "invalid HTML structure: {message}"),
            Self::Io(error) => write!(f, "I/O error: {error}"),
        }
    }
}

impl std::error::Error for HtmlError {}

pub fn rapid_path() -> PathBuf {
    std::env::current_exe()
        .ok()
        .and_then(|path| path.parent().map(|p| p.to_path_buf()))
        .unwrap_or_else(|| PathBuf::from("."))
        .join("Rapid.html")
}

pub fn load_document_from_path(path: &std::path::Path) -> Result<Document, HtmlError> {
    let content = std::fs::read_to_string(path)?;
    parse_document(&content)
}

pub fn save_document_to_path(document: &Document, path: &std::path::Path) -> Result<(), HtmlError> {
    std::fs::write(path, render_document(document))?;
    Ok(())
}

fn html_text_escape(value: &str) -> String {
    value
        .replace('&', "&amp;")
        .replace('<', "&lt;")
        .replace('>', "&gt;")
}

fn html_attr_escape(value: &str) -> String {
    value
        .replace('&', "&amp;")
        .replace('"', "&quot;")
        .replace('\'', "&#39;")
        .replace('<', "&lt;")
        .replace('>', "&gt;")
}

fn js_single_quoted_string_escape(value: &str) -> String {
    value
        .replace('\\', "\\\\")
        .replace('\n', "\\n")
        .replace('\r', "\\r")
        .replace('\'', "\\'")
        .replace('"', "\\\"")
}

fn onclick_copy_handler(body: &str) -> String {
    let js = format!("clipping('{}')", js_single_quoted_string_escape(body));
    html_attr_escape(&js)
}

pub fn render_document(document: &Document) -> String {
    let mut out = String::new();
    write_document_header(&mut out);
    let mut shortcuts = String::new();
    let mut detail_id = 0usize;
    for section in &document.sections {
        render_section(section, &mut detail_id, &mut out, &mut shortcuts);
    }
    write_document_controls(&mut out);
    out.push_str(SCRIPT_OPEN);
    out.push('\n');
    write_script_body(&mut out, &shortcuts);
    out.push_str(SCRIPT_CLOSE);
    out.push('\n');
    write_document_footer(&mut out);
    out
}

pub fn parse_document(input: &str) -> Result<Document, HtmlError> {
    if !input.contains(GENERATED_MARKER) {
        return Err(HtmlError::MissingMarker);
    }

    let mut stack: Vec<Section> = Vec::new();
    let mut root_sections = Vec::new();
    let mut in_script = false;
    let mut script_lines = Vec::new();

    for line in input.lines() {
        let trimmed = line.trim();
        if in_script {
            if trimmed == SCRIPT_CLOSE {
                in_script = false;
            } else {
                script_lines.push(trimmed.to_string());
            }
            continue;
        }

        if trimmed == SCRIPT_OPEN {
            in_script = true;
            continue;
        }

        match parse_line(trimmed)? {
            ParsedLine::Ignored => {}
            ParsedLine::OpenSection => stack.push(Section::new("")),
            ParsedLine::SectionTitle(value) => {
                let section = current_section_mut(&mut stack, "summary outside section")?;
                section.title = html_unescape(value);
            }
            ParsedLine::TextItem(value) => {
                let section = current_section_mut(&mut stack, "text item outside section")?;
                section.items.push(Item::text(html_unescape(value)));
            }
            ParsedLine::LinkItem {
                text,
                url,
                shortcut,
            } => {
                let section = current_section_mut(&mut stack, "link item outside section")?;
                section.items.push(Item::link(text, url, shortcut));
            }
            ParsedLine::MultiLinkItem {
                text,
                urls,
                shortcut,
            } => {
                let section = current_section_mut(&mut stack, "multi-link item outside section")?;
                section.items.push(Item::multi_link(text, urls, shortcut));
            }
            ParsedLine::CopyButtonItem { text, body } => {
                let section = current_section_mut(&mut stack, "copy button outside section")?;
                section.items.push(Item::copy_button(text, body));
            }
            ParsedLine::ManualLineBreak => {
                current_section_mut(&mut stack, "manual line break outside section")?
                    .items
                    .push(parsed_line_break_item())
            }
            ParsedLine::CloseSection => close_section(&mut stack, &mut root_sections)?,
        }
    }

    if !stack.is_empty() {
        return Err(invalid_structure("unclosed <details>"));
    }

    if in_script {
        return Err(invalid_structure("unclosed <script>"));
    }

    let shortcut_blocks = parse_script_shortcut_blocks(&script_lines);
    restore_multi_link_urls(&mut root_sections, &shortcut_blocks);

    Ok(Document {
        sections: root_sections,
    })
}

fn render_section(
    section: &Section,
    detail_id: &mut usize,
    out: &mut String,
    shortcuts: &mut String,
) {
    out.push_str(&format!("<details id=\"detail-{}\" open>\n", *detail_id));
    *detail_id += 1;
    out.push_str(&format!(
        "{SUMMARY_OPEN}{}{SUMMARY_CLOSE}\n",
        html_text_escape(&section.title)
    ));
    out.push_str(DETAIL_CONTENT_OPEN);
    out.push('\n');

    for item in &section.items {
        match &item.kind {
            ItemKind::Link {
                text,
                url,
                shortcut,
            } => {
                let key = shortcut_label(*shortcut);
                if let Some(key) = key {
                    out.push_str(&format!("<span class=\"key-viewer\">[{key}]</span>"));
                    push_link_shortcut(shortcuts, *shortcut, url);
                }
                out.push_str(&format!(
                    "<a href=\"{}\" target=\"_blank\" rel=\"noopener noreferrer\">{}</a><br>\n",
                    html_attr_escape(url),
                    html_text_escape(text)
                ));
            }
            ItemKind::MultiLink {
                text,
                urls,
                shortcut,
            } => {
                match shortcut_label(*shortcut) {
                    Some(key) => out.push_str(&format!(
                        "<span class=\"key-viewer-fullopen\">[{key}]</span>{}<br>\n",
                        html_text_escape(text)
                    )),
                    None => out.push_str(&format!(
                        "{KEY_VIEWER_FULLOPEN_NONE_OPEN}{}{KEY_VIEWER_FULLOPEN_NONE_MID}{}<br>\n",
                        multi_link_urls_attr_escape(urls),
                        html_text_escape(text)
                    )),
                }
                push_multi_link_shortcut(shortcuts, *shortcut, urls);
            }
            ItemKind::CopyButton { text, body } => {
                out.push_str(&format!(
                    "<button onclick=\"{}\">{} <i class=\"fa-regular fa-copy\"></i></button><br>\n",
                    onclick_copy_handler(body),
                    html_text_escape(text)
                ));
            }
            ItemKind::Text { text } => {
                out.push_str(&format!(
                    "{TEXT_ITEM_OPEN}{}{TEXT_ITEM_CLOSE}\n",
                    html_text_escape(text)
                ));
            }
            ItemKind::LineBreak => {
                out.push_str(MANUAL_LINE_BREAK);
                out.push('\n');
            }
        }
    }

    for child in &section.sections {
        render_section(child, detail_id, out, shortcuts);
    }

    out.push_str(DETAIL_CONTENT_CLOSE);
    out.push('\n');
    out.push_str(DETAILS_CLOSE);
    out.push_str("\n<br><br>\n");
}

