#[derive(Debug, PartialEq, Eq)]
enum ParsedLine<'a> {
    Ignored,
    OpenSection,
    SectionTitle(&'a str),
    TextItem(&'a str),
    LinkItem {
        text: String,
        url: String,
        shortcut: Shortcut,
    },
    MultiLinkItem {
        text: String,
        urls: String,
        shortcut: Shortcut,
    },
    CopyButtonItem {
        text: String,
        body: String,
    },
    ManualLineBreak,
    CloseSection,
}

fn parse_line(line: &str) -> Result<ParsedLine<'_>, HtmlError> {
    if is_ignored_line(line) {
        return Ok(ParsedLine::Ignored);
    }
    if is_details_open(line) {
        return Ok(ParsedLine::OpenSection);
    }
    if let Some(value) = strip_wrapped(line, SUMMARY_OPEN, SUMMARY_CLOSE) {
        return Ok(ParsedLine::SectionTitle(value));
    }
    if let Some(value) = strip_wrapped(line, TEXT_ITEM_OPEN, TEXT_ITEM_CLOSE) {
        return Ok(ParsedLine::TextItem(value));
    }
    if let Some(parsed) = parse_link_line(line) {
        return Ok(parsed);
    }
    if let Some(parsed) = parse_copy_button_line(line) {
        return Ok(parsed);
    }
    if let Some(parsed) = parse_multi_link_line(line) {
        return Ok(parsed);
    }
    if line == MANUAL_LINE_BREAK {
        return Ok(ParsedLine::ManualLineBreak);
    }
    if line == DETAILS_CLOSE {
        return Ok(ParsedLine::CloseSection);
    }

    Ok(ParsedLine::Ignored)
}

fn is_ignored_line(line: &str) -> bool {
    line.is_empty()
        || matches!(
            line,
            DOCTYPE_LINE | HTML_OPEN | HTML_CLOSE | GENERATED_MARKER | SCRIPT_OPEN | SCRIPT_CLOSE
        )
}

fn is_details_open(line: &str) -> bool {
    line == DETAILS_OPEN || line.starts_with("<details ")
}

fn strip_wrapped<'a>(line: &'a str, prefix: &str, suffix: &str) -> Option<&'a str> {
    line.strip_prefix(prefix)?.strip_suffix(suffix)
}

fn parse_link_line(line: &str) -> Option<ParsedLine<'_>> {
    let (shortcut, link_html) = if let Some(rest) = line.strip_prefix(KEY_VIEWER_OPEN) {
        let key_end = rest.find(KEY_VIEWER_CLOSE)?;
        let shortcut = parse_shortcut_label(&rest[..key_end])?;
        (shortcut, &rest[key_end + KEY_VIEWER_CLOSE.len()..])
    } else {
        (Shortcut::None, line)
    };

    let url = link_html.strip_prefix(LINK_OPEN)?;
    let url_end = url.find('"')?;
    let text_start = link_html.find('>')? + 1;
    let text_end = link_html.rfind(LINK_CLOSE)?;
    Some(ParsedLine::LinkItem {
        text: html_unescape(&link_html[text_start..text_end]),
        url: html_unescape(&url[..url_end]),
        shortcut,
    })
}

fn parse_multi_link_line(line: &str) -> Option<ParsedLine<'_>> {
    if let Some(rest) = line.strip_prefix(KEY_VIEWER_FULLOPEN_NONE_OPEN) {
        let (encoded_urls, rest) = rest.split_once(KEY_VIEWER_FULLOPEN_NONE_MID)?;
        let text = rest.strip_suffix("<br>")?;
        return Some(ParsedLine::MultiLinkItem {
            text: html_unescape(text),
            urls: js_single_quoted_string_unescape(&html_unescape(encoded_urls)),
            shortcut: Shortcut::None,
        });
    }

    let rest = line.strip_prefix(KEY_VIEWER_FULLOPEN_OPEN)?;
    let key_end = rest.find(KEY_VIEWER_CLOSE)?;
    let shortcut = parse_shortcut_label(&rest[..key_end])?;
    let text = rest[key_end + KEY_VIEWER_CLOSE.len()..].strip_suffix("<br>")?;
    Some(ParsedLine::MultiLinkItem {
        text: html_unescape(text),
        urls: String::new(),
        shortcut,
    })
}

fn parse_copy_button_line(line: &str) -> Option<ParsedLine<'_>> {
    let rest = line.strip_prefix(COPY_BUTTON_OPEN)?;
    let onclick_end = rest.find("\">")?;
    let onclick = html_unescape(&rest[..onclick_end]);
    let text = rest[onclick_end + 2..].strip_suffix(COPY_BUTTON_CLOSE)?;
    let body = parse_copy_body(&onclick)?;
    Some(ParsedLine::CopyButtonItem {
        text: html_unescape(text),
        body,
    })
}

fn parse_copy_body(onclick: &str) -> Option<String> {
    let body = onclick.strip_prefix("clipping('")?.strip_suffix("')")?;
    Some(js_single_quoted_string_unescape(body))
}

fn js_single_quoted_string_unescape(value: &str) -> String {
    let mut out = String::new();
    let mut chars = value.chars();
    while let Some(ch) = chars.next() {
        if ch != '\\' {
            out.push(ch);
            continue;
        }

        match chars.next() {
            Some('n') => out.push('\n'),
            Some('r') => out.push('\r'),
            Some('\'') => out.push('\''),
            Some('"') => out.push('"'),
            Some('\\') => out.push('\\'),
            Some(other) => {
                out.push('\\');
                out.push(other);
            }
            None => out.push('\\'),
        }
    }
    out
}

fn multi_link_urls_attr_escape(value: &str) -> String {
    html_attr_escape(&js_single_quoted_string_escape(value))
}

fn parse_shortcut_label(label: &str) -> Option<Shortcut> {
    match label {
        "Alt" => Some(Shortcut::Alt),
        "Shift + Space" | "Shift+Space" => Some(Shortcut::ShiftSpace),
        "Alt+A" => Shortcut::key('A'),
        "Alt+S" => Shortcut::key('S'),
        "Alt+D" => Shortcut::key('D'),
        other if other.len() == 1 => Shortcut::key(other.chars().next()?),
        _ => None,
    }
}

#[derive(Debug, Clone)]
enum ShortcutBlockKind {
    Unknown,
    Link,
    MultiLink,
}

#[derive(Debug, Clone)]
struct ShortcutUrls {
    kind: ShortcutBlockKind,
    shortcut: Shortcut,
    urls: Vec<String>,
}

fn parse_script_shortcut_blocks(lines: &[String]) -> Vec<ShortcutUrls> {
    let mut blocks = Vec::new();
    let mut current: Option<ShortcutUrls> = None;
    let mut pending_kind = ShortcutBlockKind::Unknown;

    for line in lines {
        if let Some(kind) = parse_script_block_kind(line) {
            pending_kind = kind;
            continue;
        }

        if let Some(shortcut) = parse_script_shortcut(line) {
            finish_shortcut_block(&mut current, &mut blocks);
            current = Some(ShortcutUrls {
                kind: pending_kind.clone(),
                shortcut,
                urls: Vec::new(),
            });
            pending_kind = ShortcutBlockKind::Unknown;
            continue;
        }

        if let Some(block) = current.as_mut() {
            if let Some(url) = parse_window_open_literal_url(line) {
                block.urls.push(url);
                continue;
            }

            if line.starts_with('}') {
                finish_shortcut_block(&mut current, &mut blocks);
            }
        }
    }

    finish_shortcut_block(&mut current, &mut blocks);

    blocks
}

fn finish_shortcut_block(current: &mut Option<ShortcutUrls>, blocks: &mut Vec<ShortcutUrls>) {
    if let Some(block) = current.take()
        && !block.urls.is_empty()
    {
        blocks.push(block);
    }
}

fn parse_script_block_kind(line: &str) -> Option<ShortcutBlockKind> {
    match line {
        LINK_SHORTCUT_MARKER => Some(ShortcutBlockKind::Link),
        MULTI_LINK_SHORTCUT_MARKER => Some(ShortcutBlockKind::MultiLink),
        _ => None,
    }
}

fn parse_script_shortcut(line: &str) -> Option<Shortcut> {
    if line.contains("shiftKey") && (line.contains("keyCode == 32") || line.contains("key === ' '"))
    {
        return Some(Shortcut::ShiftSpace);
    }

    if let Some(shortcut) = parse_script_letter_shortcut(line) {
        return Some(shortcut);
    }

    if line.contains("altKey") {
        return Some(Shortcut::Alt);
    }

    None
}

fn parse_script_letter_shortcut(line: &str) -> Option<Shortcut> {
    for code in b'A'..=b'Z' {
        let uppercase = char::from(code);
        let lowercase = uppercase.to_ascii_lowercase();
        let key_code = format!("keyCode == {code}");
        let single_quoted = format!("key === '{lowercase}'");
        let double_quoted = format!("key === \"{lowercase}\"");
        let uppercase_single_quoted = format!("key === '{uppercase}'");
        let uppercase_double_quoted = format!("key === \"{uppercase}\"");

        if line.contains(&key_code)
            || line.contains(&single_quoted)
            || line.contains(&double_quoted)
            || line.contains(&uppercase_single_quoted)
            || line.contains(&uppercase_double_quoted)
        {
            return Shortcut::key(uppercase);
        }
    }

    None
}

fn parse_window_open_literal_url(line: &str) -> Option<String> {
    let (_, rest) = line.split_once("window.open('")?;
    let url = rest.split_once("','")?.0;
    Some(js_single_quoted_string_unescape(&html_unescape(url)))
}

fn restore_multi_link_urls(sections: &mut [Section], shortcut_blocks: &[ShortcutUrls]) {
    let mut remaining = shortcut_blocks.to_vec();
    restore_multi_link_urls_in_sections(sections, &mut remaining);
}

fn restore_multi_link_urls_in_sections(
    sections: &mut [Section],
    remaining: &mut Vec<ShortcutUrls>,
) {
    for section in sections {
        for item in &mut section.items {
            if let ItemKind::MultiLink { urls, shortcut, .. } = &mut item.kind
                && let Some(index) = remaining.iter().position(|block| {
                    block.shortcut == *shortcut && !matches!(block.kind, ShortcutBlockKind::Link)
                })
            {
                let block = remaining.remove(index);
                *urls = block.urls.join("\n");
            }
        }
        restore_multi_link_urls_in_sections(&mut section.sections, remaining);
    }
}

